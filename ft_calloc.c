#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	unsigned int	s;
	char			*ptr;

	s = nmemb * size;
	ptr = malloc(s);
	if (ptr == NULL)
		return (NULL);
	ft_memset(ptr, 0, s);
	return (ptr);
}
