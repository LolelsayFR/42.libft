#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	result;

	if (n <= -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
		return ;
	}
	result = n + '0';
	write(fd, &result, 1);
}
